    private void loadInDocotrs() {
        ArrayList<User> users = PatientManagementSystem.getUsers();
         for (int i = 0; i < users.size(); i++) {
            String userID = users.get(i).getUniqueID();
            if(userID.contains("D")){
                this.CbxDoctorSelector.addItem("Dr "+users.get(i).getFirstName()+" "+users.get(i).getLastName());
            }
        }
        if(this.CbxDoctorSelector.getItemCount() == 0) this.CbxDoctorSelector.addItem("No Doctors Loaded");
    }
