    private void BtnSaveReviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveReviewActionPerformed
        ArrayList<User> users = PatientManagementSystem.getUsers();
        for(User user: users){
            if(user.getUniqueID().contains("D")){
                    if(this.CbxDoctorSelector.getSelectedItem().toString().contains(user.getFirstName()) &&
                        this.CbxDoctorSelector.getSelectedItem().toString().contains(user.getLastName())){
                        Doctor doctor = (Doctor) user;
                        Rating rating = new Rating((int) this.SpinRating.getValue(),this.TxtComments.getText());
                        doctor.getRatings().add(rating);
                        PatientManagementSystem.saveInformation("test//Data_1.txt");
                        this.php.updateRatingsList();
                        this.dispose();
                    }
            }
        }
    }//GEN-LAST:event_BtnSaveReviewActionPerformed

